# lstrip , rstrip and strip function is used for removing space
n="          This is demo paragraph. I am panditprogrammer"
# lstrip is used for removing left side space
# rstrip is used for removing rightside space
print(n.lstrip())
print(n.rstrip())
#strip function is used for removing space
m="     My name is Chandan kumar.      "
print(m.strip())