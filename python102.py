# String function startswith and endswith in python
# result shown in True or False
n="Hi , my name chandan."
print("This is checking startswith function")
print(n.startswith("this"))
print(n.startswith("Hi"))
print(n.startswith("H"))
# endswith function
print("\n")
print(n.endswith('who'))
print(n.endswith('chandan'))
print(n.endswith('dan'))
print(n.endswith('dan.'))
print(n.endswith("."))
print(n.endswith("chandan."))
